    private void ShowData() {
        model.setRowCount(0);
        for (ChiTietPhieuGiaoHang ct : list) {
            model.addRow(new Object[]{ct.getMaChiTiet(),ct.getId(),ct.getMaSanPham(),ct.getSoLuong(),ct.getGiaBan()});
        }
    }
    
    private void showViTri(int vitri) {
        ChiTietPhieuGiaoHang gh = list.get(vitri);
        txtId.setText(gh.getId()+"");
        txtMaChiTiet.setText(gh.getMaChiTiet());
        txtMaSanPham.setText(gh.getMaSanPham());
        txtSoLuong.setText(gh.getSoLuong()+"");
        txtGiaBan.setText(gh.getGiaBan()+ "");
        
    }
    
    private ChiTietPhieuGiaoHang getForm() {
        ChiTietPhieuGiaoHang ct = new ChiTietPhieuGiaoHang();
        ct.setId(txtId.getText());
        ct.setMaChiTiet(txtMaChiTiet.getText());
        ct.setMaSanPham(txtMaSanPham.getText());
        ct.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        ct.setGiaBan(new BigDecimal(txtGiaBan.getText()));
        return ct;
    }

