    private void ShowData() {
        model.setRowCount(0);
        for (DiaChiGiaoHang dc : list) {
            model.addRow(new Object[]{dc.getMaDiaChi(),dc.getDiaChi(),dc.getGhiChu()});
        }
    }
    
    private void showViTri(int vitri) {
        DiaChiGiaoHang dc = list.get(vitri);
        txtMaDiaChi.setText(dc.getMaDiaChi());
        txtDiaChi.setText(dc.getDiaChi());
        txtGhiChu.setText(dc.getGhiChu());  
    }
    
    private DiaChiGiaoHang getForm() {
       DiaChiGiaoHang ct = new DiaChiGiaoHang();
        ct.setMaDiaChi(txtMaDiaChi.getText());
        ct.setDiaChi(txtDiaChi.getText());
        ct.setGhiChu(txtGhiChu.getText());
        return ct;
    }

