    private void btnThem_NhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem_NhanVienActionPerformed
        Boolean ThemNV = nhanVienServices.AddNhanVien(getForm());
        lists = nhanVienServices.getAll();
        showDataa(lists);
        if (ThemNV) {
            JOptionPane.showConfirmDialog(this, "Add thành công");
        } else {
            JOptionPane.showConfirmDialog(this, "Add thất bại");
        }
    }//GEN-LAST:event_btnThem_NhanVienActionPerformed

    private void btnSua_NhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSua_NhanVienActionPerformed
        int row = tblNhanVien.getSelectedRow();
        nhanVienServices.Update(getForm(), lists.get(row).getMaNV());
        lists = nhanVienServices.getAll();
        showDataa(lists);
        JOptionPane.showConfirmDialog(this, "Cập nhật thành công");
    }//GEN-LAST:event_btnSua_NhanVienActionPerformed

    private void btnXoa_NhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa_NhanVienActionPerformed

        try {
            int traloi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa");
            if (traloi == JOptionPane.YES_OPTION) {
                nhanVienServices.XoaNV(txtMaNV.getText());
                lists = nhanVienServices.getAll();
                showDataa(lists);
                JOptionPane.showConfirmDialog(this, "Xóa thành công");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnXoa_NhanVienActionPerformed

