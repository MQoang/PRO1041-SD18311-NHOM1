    private void showDataa(List<NhanVien> nhanVien) {
        tblmodel.setRowCount(0);
        for (NhanVien nv : nhanVien) {
            tblmodel.addRow(new Object[]{
                nv.getId(), nv.getMaNV(), nv.getTenNV(), nv.getGioiTinh() == true ? "Nam" : "Nu", nv.getNgaySinh(), nv.getSđt(), nv.getEmail(),
                nv.getChuThich(), nv.getChucVu(), nv.getNgayTuyen(), nv.getLuong(), nv.getCalam(), nv.getThoigian(), nv.getDiaChi()
            });
        }
    }

    private NhanVien getForm() {
        NhanVien nv = new NhanVien();
        String id = txtID.getText().trim();
        nv.setId(Integer.valueOf(id));
        nv.setMaNV(txtMaNV.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setNgayTuyen(LocalDate.parse(txtNgayLam.getText()));
        nv.setNgaySinh(LocalDate.parse(txtNgagSinh.getText()));
        nv.setChuThich(txtChuThich_NhanVien.getText());
        nv.setChucVu((String) cbbChucVu_NhanVien.getSelectedItem());
        nv.setSđt(txtSoDT_NhanVien.getText());
        nv.setEmail(txtEmail.getText());
        nv.setDiaChi(txtDiaChi_NhanVien.getText());
        Boolean gioTinh = rbtnNam_NhanVien.isSelected();
        if (gioTinh == true) {
            nv.setGioiTinh(true);
        } else {
            nv.setGioiTinh(false);
        }
        String phoneNumber = txtSoDT_NhanVien.getText();
        if (!phoneNumber.matches("^0\\d{9}$")) {
            JOptionPane.showMessageDialog(this, "ERROR: Số điện thoại không đúng định dạng! ");
            return null;
        }
        return nv;
    }

    private void detail(int viTri) {
        NhanVien nv = lists.get(viTri);
        txtID.setText(nv.getId() + "");
        txtMaNV.setText(nv.getMaNV());
        txtTenNV.setText(nv.getTenNV());
        txtNgagSinh.setText(nv.getNgaySinh().toString());
        cbbChucVu_NhanVien.setSelectedItem(nv.getChucVu());
        txtNgayLam.setText(nv.getNgayTuyen().toString());
        cboCaLam.setSelectedItem(nv.getCalam());
        cboThoiGian.setSelectedItem(nv.getThoigian());
        txtEmail.setText(nv.getEmail());
        txtLuong.setText(nv.getLuong() + "");
        txtSoDT_NhanVien.setText(nv.getSđt());
        txtDiaChi_NhanVien.setText(nv.getDiaChi());
        txtChuThich_NhanVien.setText(nv.getChuThich());
        Boolean gioiTinh = nv.getGioiTinh();
        if (gioiTinh == true) {
            rbtnNam_NhanVien.setSelected(true);
        } else {
            rbtnNu_NhanVien.setSelected(true);
        }
    }
