    public DotGiamGia getForm() {
        DotGiamGia dgg = new DotGiamGia();
        String ma = txtMaDotGiamGia.getText().trim();
        String ten = txtTen.getText().trim();
        String id = txtId.getText().trim();
        Date batDau = jdcBatDau.getDate();
        Date ketThuc = jdcKetThuc.getDate();
        String moTa = txtMoTa.getText().trim();
        String dieuKien = txtDieuKien.getText().trim();
        StringBuilder sb = new StringBuilder();

        dgg.setMaDot(ma);
        dgg.setTenDot(ten);
        dgg.setId(Integer.valueOf(id));
        dgg.setNgayBD(batDau);
        dgg.setNgayKT(ketThuc);
        dgg.setMota(moTa);
        dgg.setDieuKienApDung(dieuKien);
        return dgg;
    }

    public void showDetail(int index) {
        DotGiamGia dgg = dsdot.get(index);
        txtMaDotGiamGia.setText(dgg.getMaDot());
        txtId.setText(String.valueOf(dgg.getId()));
        txtTen.setText(dgg.getTenDot());
        txtDieuKien.setText(dgg.getDieuKienApDung());
        jdcBatDau.setDate(dgg.getNgayBD());
        jdcKetThuc.setDate(dgg.getNgayKT());
        txtPhanTram.setText(String.valueOf(dgg.getPhanTram() + " %"));
        txtSoLuong.setText(String.valueOf(dgg.getSoLuong()));
        txtMoTa.setText(dgg.getMota());
    }

