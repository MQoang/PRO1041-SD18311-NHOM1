    private void showDuLieu(List<DotGiamGia> list) {
        tblModel.setRowCount(0);
        for (DotGiamGia dot : list) {
            tblModel.addRow(new Object[]{
                dot.getMaDot(), dot.getId(), dot.getTenDot(), dot.getNgayBD(), dot.getNgayKT(), dot.getDieuKienApDung(), dot.getPhanTram(), dot.getSoLuong(), dot.getMota()
            });
        }
    }

    public void showData(List<MaVoucher> list) {
        tbm.setRowCount(0);
        for (MaVoucher ma : list) {
            tbm.addRow(new Object[]{
                ma.getId(), ma.getPhanTram(), ma.getSoLuong()
            });
        }
    }

